/**
 * Makes modules variables visible in global scope
 * @type {{makeVisible: provideGlobal.makeVisible}}
 */
const provideGlobal = {
    makeVisible: function (variable, name, scope = window) {
        scope[name] = variable;
    }
}
export default provideGlobal;