function searchList() {

  let value = $(this).val().toLowerCase();

  $("#currencyList li").filter(function() {
    $(this).toggle($(this).text().toLowerCase().indexOf(value) > -1);

  });
}

$('#searchCurrency').keyup(searchList)

export default {};